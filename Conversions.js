// 1. Implicit
console.log(3 + "3");
console.log(true + "3");
console.log(3 + true);
console.log("3" + "3");

console.log(3 - "3");
console.log(true - "3");
console.log(3 - true);
console.log("6" - "3");

// 2. Explicit
console.log(Number(5));
console.log(Number('abc'));
console.log(parseInt(10));
console.log(parseFloat(3.14));

